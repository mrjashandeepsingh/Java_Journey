public class DataType {
    public static void main(String args[]) {
        // Integer Datatypes 
        int a = 192356;
        byte b = 127;
        short c = 230;
        long d = 234542326;

        // Floating Datatypes
        float e = 1223.2324f;
        double f = 2342.3423;

        // characters and boolean Datatypes
        char g = 'd';
        boolean h = true;
    }
}